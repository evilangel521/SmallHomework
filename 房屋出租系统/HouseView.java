package 面向对象中级部分.房屋出租系统;

/**
1.显示界面
2.接受用户的输入
3.调用HouseService完成对房屋信息的各种操作
 */
public class HouseView {
    private boolean loop = true;
    private char key = ' ';//接受用户选择
    private HouseService houseService = new HouseService(10);//设置数组大小为10

    //addHouse()接收输入，创建house对象，调用add方法
    public void addHouse(){
        System.out.println("=========添加房屋=========");
        System.out.print("姓名：");
        String name = utility.readString(8);
        System.out.print("电话：");
        String phone = utility.readString(12);
        System.out.print("地址：");
        String address = utility.readString(16);
        System.out.print("月租：");
        int rent = utility.readInt();
        System.out.print("状态：");
        String state = utility.readString(3);
        //id是系统分配的
        House house = new House(0, name, phone, address, rent, state);
        if (houseService.add(house)){
            System.out.println("添加房屋成功");
        }else {
            System.out.println("添加房屋失败");
        }
    }

    //根据id查找房屋信息
    public void findHouse(){
        System.out.println("=========查找房屋信息=========");
        System.out.println("请输入要查找的id");
        int findId = utility.readInt();
        //调用方法
        House house = houseService.findById(findId);
        if (house != null){
            System.out.println(house);
        }else {
            System.out.println("=========查找房屋信息id不存在=========");
        }
    }

    //编写delHouse（）接受输入的id，调用Service的del方法
    public void delHouse() {
        System.out.println("=========删除房屋=========");
        System.out.println("请输入待删除房屋的编号（-1退出）");
        int delId = utility.readInt();
        if (delId == -1) {
            System.out.println("==========放弃删除房屋信息=========");
            return;
        }
        //注意该方法本身就有循环判断的逻辑，必须输出Y/N
        char choice = utility.readConfirmSelection();
        if (choice == 'Y') {//确定删除
            if (houseService.del(delId)) {
                System.out.println("=========删除房屋信息成功=========");

            } else {
                System.out.println("=========房屋编号不存在，删除失败");
            }
        } else {
            System.out.println("=========放弃删除房屋信息=========");
        }
    }

    //修改
    public void update(){
        System.out.println("=========修改房屋信息=========");
        System.out.println("请选择待修改房屋编号（-1表示退出）");
        int updateId = utility.readInt();
        if (updateId == -1){
            System.out.println("=========放弃修改房屋信息=========");
            return;
        }
        //根据输入得到updateId，查找对象
        //返回的是引用类型，即：返回就是数组的元素
        //在后面对house.setXxx（），就会修改HouseService中houses数组的元素
        House house = houseService.findById(updateId);
        if (house == null) {
            System.out.println("=========修改房屋信息编号不存在=========");
            return;
        }

        System.out.print("姓名(" + house.getName() + "): ");
        //这里如果用户直接回车表示不修改信息 默认""
        String name = utility.readString(8, "");
        if (!"".equals(name)) {//如果不是“”就要修改
            house.setName(name);
        }
        System.out.print("电话(" + house.getPhone() + "):");
        String phone = utility.readString(12, "");
        if (!"".equals(phone)) {
            house.setPhone(phone);
        }
        System.out.print("地址(" + house.getAddress() + "):");
        String address = utility.readString(18, "");
        if (!"".equals(address)) {
            house.setAddress(address);
        }
        System.out.print("租金(" + house.getRent() + "):");
        int rent = utility.readInt(-1);
        if (rent != -1) {
            house.setRent(rent);
        }
        System.out.println("状态(" + house.getState() + "):");
        String state = utility.readString(3, "");
        if (!"".equals(state)) {
            house.setState(state);
        }
        System.out.println("===========修改房屋信息成功=========");
    }

    //退出
    public void exit(){
        char c = utility.readConfirmSelection();
        if (c == 'Y'){
            loop = false;
        }
    }
    public void listHouse(){//显示房屋列表
        System.out.println("=========房屋列表=========");
        System.out.println("编号\t\t房主\t\t电话\t\t地址\t\t月租\t\t状态（未出租/已出租）");
        House[] houses = houseService.list();//得到所有房屋信息
        for (int i = 0; i < houses.length; i++) {
            if (houses[i] == null){//如果结果为null，就不显示了
                break;
            }
            System.out.println(houses[i]);
        }
        System.out.println("========房屋列表显示完毕========");
    }

    //显示主菜单
    public void MainMenu(){

        do {
            System.out.println("\n=========房屋出租系统=========");
            System.out.println("\t\t1.新 增 房 源");
            System.out.println("\t\t2.查 找 房 源");
            System.out.println("\t\t3.删 除 房 屋 信 息");
            System.out.println("\t\t4.修 改 房 屋 信 息");
            System.out.println("\t\t5.房 屋 列 表");
            System.out.println("\t\t6.退      出");
            System.out.print("请输入你的选择（1-6）：");

            key = utility.readChar();

            switch (key){
                case '1':
                    addHouse();
                    break;
                case '2':
                    findHouse();
                    break;
                case '3':
                    delHouse();
                    break;
                case '4':
                    update();
                    break;
                case '5':
                    listHouse();
                    break;
                case '6':
                    exit();
                    break;
            }
        }while (loop);
    }

}
